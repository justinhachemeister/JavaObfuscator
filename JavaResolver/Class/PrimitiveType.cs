namespace JavaResolver.Class.Code
{
    /// <summary>
    /// Provides all possible primitive types that can be referenced in a Java class file.
    /// </summary>
    public enum PrimitiveType : byte
    {
        Boolean = 4,
        Char = 5,
        Float = 6,
        Double = 7,
        Byte = 8,
        Short = 9,
        Int = 10,
        Long = 11,
    }
}